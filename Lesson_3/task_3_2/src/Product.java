class Product {
    private int weight;
    private int value;
    private String name;

    public Product(int weight, int value, String name) {
        this.weight = weight;
        this.value = value;
        this.name = name;
    }
    public int getWeight() {
        return weight;
    }

    public int getValue() {
        return value;
    }

    public String getName() {
        return name;
    }


    public String toString() {
        return name;
    }

}
